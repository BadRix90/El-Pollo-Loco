class Keyboard {


    /**
 * Represents the current state of keyboard inputs.
 * Stores boolean flags for specific keys used in the game.
 */
    constructor() {
        this.LEFT = false;
        this.RIGHT = false;
        this.UP = false;
        this.DOWN = false;
        this.SPACE = false;
        this.SHIFT = false;
        this.CTRL = false;
        this.q = false;
    }
}
